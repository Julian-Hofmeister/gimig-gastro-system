import { Injectable } from '@angular/core';

@Injectable({
  providedIn: 'root'
})
export class EditService {
  //#region [ PROPERTIES ] /////////////////////////////////////////////////////////////////////////

  editMode = false;

  // ----------------------------------------------------------------------------------------------

  //#endregion

  //#region [ CONSTRUCTORS ] //////////////////////////////////////////////////////////////////////

  constructor() {}

  //#endregion

  //#region [ PUBLIC ] ////////////////////////////////////////////////////////////////////////////

  enableEditMode() {
    this.editMode = true;
  }

  // ----------------------------------------------------------------------------------------------

  disableEditMode() {
    this.editMode = false;
  }

  // ----------------------------------------------------------------------------------------------

  getEditModeStatus() {
    return this.editMode;
  }

  // ----------------------------------------------------------------------------------------------

  //#endregion

  //#region [ PRIVATE ] ///////////////////////////////////////////////////////////////////////////

  // ----------------------------------------------------------------------------------------------

  //#endregion
}
